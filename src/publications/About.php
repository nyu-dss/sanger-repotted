<p>Foreword by Peter C. Engelman.</p><iframe src="https://rcm-na.amazon-adsystem.com/e/cm?t=margsangpapep-20&o=1&p=8&l=as1&asins=1591020581&fc1=000000&IS2=1&lt1=_blank&m=amazon&lc1=0000FF&bc1=000000&bg1=FFFFFF&f=ifr" style="width:180px;height:300px;" scrolling="no" marginwidth="0" marginheight="0" frameborder="0" align="right"></iframe>
<p>Arguably her most important and influential book, this controversial work, first published in 1922 by pioneering birth-control advocate Margaret Sanger, attempted to broaden the still-radical idea of birth control beyond its socialist and feminist roots. Moving away from a single-minded focus on women's reproductive rights to the larger issue of the general health and economic prosperity of the whole human race, Sanger argued that birth control was pivotal to a rational approach toward dealing with the threat of overpopulation and its ruinous consequences in poverty and disease. Through this book, Sanger hoped to persuade the medical establishment to assume control over contraceptive distribution, and thereby to lessen the religious, legal, and moral opposition that continued to restrict access to contraceptive information. She also introduced to Americans the link between science and sex, which hitherto had been found mainly in the work of European sexologists. </p>
