<p>The first goal of the Margaret Sanger Papers Project was to gather together her voluminous personal papers and make them readily available to the public. Preserving 
them on microfilm guaranteed their survival and enabled researchers who were not located at the repositories that held the collections to search
through the entire corpus of Sanger's Papers.</p>

<p>From 1986-1996, the Margaret Sanger Papers Project organized and microfilmed the Margaret Sanger papers in the Sophia Smith Collection at Smith College and 
from other respositories. We organized, assembled the documents, and produced astate of the art microfilm edition of over 60,000 papers and records divided 
into <b>two series:</b> the <em><a href="smith_series.php">Smith College Collections</a></em> and <em><a href=<"collected_documents.php">Collected Documents</a></em>. <em>The 
Margaret Sanger Papers Microfilm Edition</em> (101 reels) was published in 1996 by University Publications of America (now <a href="http://cisupa.proquest.com/ws_display.asp?filter=upa_intermediate&item_id={8A05BFDC-6146-42E4-BC54-1D887B081E00}">Proquest</a>, 
with a printed reel guide and name and title index to each series.  For more, see<a href="microfilm.php">Microfilm Edition</a></p>

<p>In 2018, Proquest digitized these two microfilm series and made them available online in its History Vault. The ProQuest® History Vault provides access to millions of primary source,
 cross-searchable, full-text/full-image documents on the most widely studied topics in 19th and 20th-century American history. The vast majority of the content
in History Vault is not available elsewhere. The content in History Vault is perfect for researchers in history, African American studies, women’s studies, political
 science, social sciences, sociology, and international studies. For more on the History Vault and its guides, see <a href="https://proquest.libguides.com/historyvault/contents">Proquest History Vault.</a></p>

<p> We have been continuing to gather Sanger documents. Proquest will be adding these documents, collected since 1998, to its History Vault edition later this year.</p>  

