<p>The <i>Margaret Sanger Papers Microfilm Edition:Smith College Collections</i>, published in
1996 by University Publications of America, (now <a href="https://cisupa.proquest.com/ws_display.asp?filter=upa_leaf&item_id={045AB6D6-ABC6-439D-ACFB-F7A37718D427}">ProQuest</a>) contains nearly 45,000 documents drawn from the Margaret Sanger Papers and nineteen other collections 
of manuscript material and archival records located in the Sophia Smith Collection and Smith College Archives at Smith College. 
It incorporates extensive correspondence, writings, and organizational records chronicling Sanger's career as the founder and life-long leader of the American and international 
birth control movements; and rich documentation of her personal life, friendships and family.</p>

<p>For more information on the collection, see the <a href="https://cisupa.proquest.com/ws_display.asp?filter=upa_leaf&item_id=%7B045AB6D6-ABC6-439D-ACFB-F7A37718D427%7D">Proquest</a> site. </p>

<p>For the <a href="SmithGuide.pdf">Guide and Index</a> in PDF format (note: it is a large file). </p> 

<h2>Organization</h2>
<p>Documents in these two series were carefully described using item-level targets and arranged into the following five subseries:</p>

<blockquote>
<p>Subseries 1: Correspondence
	<br />Sanger's incoming and outgoing correspondence, as well as letters written and received on her behalf by her secretaries.</p>
    
<p>Subseries 2: Organizational Records/Conference Materials
<br />	Organizational records, including constitutions, by-laws, minutes, publications, form letters, and reports created by birth control organizations which Sanger established, led, or in which she played a significant role.  This subseries also includes proceedings, programs and other records related to conferences in the U.S. and abroad which were organized or attended by Sanger.</p>

<p>Subseries 3: Legal/Governmental Material
	<br />Records relating to the trials in which Sanger or one of the birth control organizations she led was a participant, and records of government legislation she sponsored.</p>
    
<p>Subseries 4: Writings
<br />	Diaries, journals, speeches, articles, unpublished manuscripts, and statements and miscellaneous writings by Sanger.</p>

<p>Subseries 5: Miscellany
<br />	Miscellaneous material that chronicles Sanger's non-birth control activities, family, homes, and hobbies.  Also includes tributes to Sanger, itineraries, calendars, appointment books, financial records and notes and biographical information.</p>
</blockquote>

<h2>Reel Guide</h2>
<p>To provide access to the documents in the microfilm edition, the Project prepared printed guides to each of its microfilmed series.  Each guide includes an introduction to the collection, an essay on editorial principles and methodology, histories of each of the seventeen organizations Sanger founded or led, brief reel descriptions, and an item-level name/title index. The introductory material, but not the indexes, are available on the Lexis-Nexis website. <!-- <em>To access the reel guide material, click <a href="http://www.lexisnexis.com/academic/guides/womens_studies/margaret_sanger/sanger.asp">here</a>. </em>--></p>

